comp_answers = ["rock", "paper", "scissors", "lizard", "spock"]

def computer_win
	puts "Puny human....your brain is no match for me. You lose this round."
	@computer += 1
end

def human_win
	puts "You only win because I let you win."
	@human += 1
end

def playing_game(user_answer, comp_answer)

	if user_answer == comp_answer
		puts "Does not compute. Let's call it a draw..."

	elsif user_answer == "rock" && comp_answer == "paper"
		computer_win

	elsif user_answer == "rock" && comp_answer == "scissors"
		human_win

	elsif user_answer == "rock" && comp_answer == "spock"
		computer_win

	elsif user_answer == "rock" && comp_answer == "lizard"
		human_win

	elsif user_answer == "paper"  && comp_answer == "rock"
		human_win

	elsif user_answer == "paper" && comp_answer == "scissors"
		computer_win

	elsif user_answer == "paper" && comp_answer == "spock"
		human_win

	elsif user_answer == "paper" && comp_answer == "lizard"
		computer_win

	elsif user_answer == "scissors" && comp_answer == "rock"
		computer_win

	elsif user_answer == "scissors" && comp_answer == "paper"
		human_win

	elsif user_answer == "scissors" && comp_answer == "spock"
		computer_win

	elsif user_answer == "scissors" && comp_answer == "lizard"
		human_win

	elsif user_answer == "lizard" && comp_answer == "rock"
		computer_win

	elsif user_answer == "lizard" && comp_answer == "paper"
		human_win

	elsif user_answer == "lizard" && comp_answer == "scissors"
		computer_win

	elsif user_answer == "lizard" && comp_answer == "spock"
		human_win

	elsif user_answer == "spock" && comp_answer == "rock"
		human_win

	elsif user_answer == "spock" && comp_answer == "paper"
		computer_win

	elsif user_answer == "spock" && comp_answer == "scissors"
		human_win

	elsif user_answer == "spock" && comp_answer == "lizard"
		computer_win

	else
		puts "Enter a real answer wise guy."
	end
end

